def summarize_samples(samples: Sequence[float]) -> Dict[str, float]:
    if not samples:
        return {"count": 0}
    n = len(samples)
    out = {
        "count": n,
        "mean":  sum(samples) / n,
        "min":   min(samples),
        "max":   max(samples),
    }
    if n > 1:
        out["std"] = statistics.stdev(samples)
        s = sorted(samples)
        out["p50"] = s[n // 2]
        out["p99"] = s[min(n - 1, int(n * 0.99))]
    return out


def flatten_profile_into_row(row: Dict[str, Any],
                              snap: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """Flatten a profile snapshot into ``timer.<name>.<stat>`` columns."""
    if not snap:
        return row
    for tname, tstats in (snap.get("timers") or {}).items():
        for stat, val in tstats.items():
            row[f"timer.{tname}.{stat}"] = val
    for gname, gstats in (snap.get("gauges") or {}).items():
        for stat, val in gstats.items():
            row[f"gauge.{gname}.{stat}"] = val
    for cname, cval in (snap.get("counters") or {}).items():
        row[f"counter.{cname}"] = cval
    return row


