def clean_hook_dir(hook_dir: str) -> None:
    """Remove all run subdirectories under ``hook_dir`` (artifacts pile up
    quickly when sweeping many cells against tmpfs)."""
    if not hook_dir or not os.path.isdir(hook_dir):
        return
    for entry in os.listdir(hook_dir):
        full = os.path.join(hook_dir, entry)
        if os.path.isdir(full):
            shutil.rmtree(full, ignore_errors=True)
