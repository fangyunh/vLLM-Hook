def files_size_kb(paths: Iterable[str]) -> float:
    return sum(os.path.getsize(p) for p in paths if os.path.isfile(p)) / 1024.0


def dir_size_kb(path: str) -> float:
    total = 0
    for root, _dirs, files in os.walk(path):
        for f in files:
            try:
                total += os.path.getsize(os.path.join(root, f))
            except OSError:
                pass
    return total / 1024.0


def wait_for_artifact(hook_dir: str, run_id: str, *, use_safetensors: bool,
                      timeout_s: float = 10.0) -> bool:
    """Poll for the artifact under ``hook_dir/run_id``.

    Safetensors path: requires BOTH ``*.safetensors`` and ``*.json``.
    .pt path: any file under the run dir is sufficient.
    """
    import glob as _glob
    deadline = time.time() + timeout_s
    base = os.path.join(hook_dir, run_id)
    while time.time() < deadline:
        if use_safetensors:
            st = _glob.glob(os.path.join(base, "**", "*.safetensors"), recursive=True)
            js = _glob.glob(os.path.join(base, "**", "*.json"),         recursive=True)
            if st and js:
                return True
        else:
            if _glob.glob(os.path.join(base, "**", "*"), recursive=True):
                return True
        time.sleep(0.01)
    return False


